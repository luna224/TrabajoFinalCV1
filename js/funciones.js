const button = document.getElementById("boton");
const link = document.getElementById("link");

link.style.display = "none";

button.addEventListener("click",(event)=>{

    if (link.style.display == "none"){
        link.style.display = "block"
    } else {
        link.style.display = "none"
    }
})
